package src.main.java.game;

public class Coordinate {
    private int xCoordinate;
    private int yCoordinate;

    public void setX(int x){

        this.xCoordinate = x;
    }
    public int getX(){

        return xCoordinate;
    }
    public void setY(int y){

        this.yCoordinate = y;
    }
    public int getY(){

        return yCoordinate;
    }

}
